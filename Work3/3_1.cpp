#include <iostream>
using namespace std;

int main() {
    cout << "Result of 3 / 2 + 5 = " << 3 / 2 + 5 << endl;
    cout << "Result of 5 / 1 + 2 = " << 5 / 1 + 2 << endl;
    cout << "Result of 3 * 2 + 4 * 5 = " << 3 * 2 + 4 * 5 << endl;
    cout << "Result of 4 - 2 + 5 / 3 + 2 = " << 4 - 2 + 5 / 3 + 2 << endl;
    cout << "Result of 10 - 2 + 7 % 2 - 1 = " << 10 - 2 + 7 % 2 - 1 << endl;
    return 0; 
}